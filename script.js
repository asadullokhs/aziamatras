let lang = "uz";
let currentMattress = null;

function setLang(l) {
  lang = l;

  // Toggle Active Buttons
  document
    .querySelectorAll(".lang-btn")
    .forEach((b) =>
      b.classList.toggle("active", b.textContent.toLowerCase() === l),
    );

  // Translate all matching data-t attributes
  document.querySelectorAll("[data-t]").forEach((el) => {
    const k = el.getAttribute("data-t");
    if (T[l][k] !== undefined) {
      if (k === "hero_h1") {
        el.innerHTML = T[l][k];
      } else {
        el.textContent = T[l][k];
      }
    }
  });

  // Re-render open item details if active
  if (currentMattress) renderDetail(currentMattress);
}

function openDetail(id) {
  currentMattress = id;
  renderDetail(id);

  document.getElementById("detail-overlay").classList.add("show");

  document.body.style.overflow = "hidden";
}

function renderDetail(id) {
  const d = MATTRESSES[id][lang];
  document.getElementById("d-name").textContent = d.name;
  document.getElementById("d-price").textContent = d.price;
  document.getElementById("d-tagline").textContent = d.tagline;

  document.getElementById("d-specs").innerHTML = d.specs
    .map(
      (s) =>
        `<div class="spec-row"><span class="spec-k"><strong>${s[0]}:</strong> </span><span class="spec-v">${s[1]}</span></div>`,
    )
    .join("");

  document.getElementById("d-benefits").innerHTML = d.benefits
    .map(
      (b) =>
        `<div class="benefit-row"><span class="b-arrow">→ </span><span>${b}</span></div>`,
    )
    .join("");

  // Update dynamic modal structural words
  document
    .querySelectorAll('[data-t="specs_title"]')
    .forEach((el) => (el.textContent = T[lang]["specs_title"]));
  document
    .querySelectorAll('[data-t="benefits_title"]')
    .forEach((el) => (el.textContent = T[lang]["benefits_title"]));
  document
    .querySelectorAll('[data-t="close_btn"]')
    .forEach((el) => (el.textContent = T[lang]["close_btn"]));
  document
    .querySelectorAll('[data-t="order_btn"]')
    .forEach((el) => (el.textContent = T[lang]["order_btn"]));
}

function closeDetail() {
  document.getElementById("detail-overlay").classList.remove("show");

  document.body.style.overflow = "";

  currentMattress = null;
}

function closeOutside(e) {
  if (e.target === document.getElementById("detail-overlay")) closeDetail();
}


